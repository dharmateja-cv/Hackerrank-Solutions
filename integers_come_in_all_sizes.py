def compute():
    a = int(input("Enter the integer 'a' : "))
    b = int(input("Enter the integer 'b' : "))
    c = int(input("Enter the integer 'c' : "))
    d = int(input("Enter the integer 'd' : "))

    print((a**b) + (c**d))

def main():
    print()
    print("Hackerrank : Integers come in all sizes ")
    print()

    compute()

main()