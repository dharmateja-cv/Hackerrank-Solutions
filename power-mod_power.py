# power-mod_power.py

def compute():
    a = int(input("Enter the value of a : "))
    b = int(input("Enter the value of b : "))
    m = int(input("Enter the value of m : "))

    print(pow(a , b))
    print(pow(a , b , m))

def main():
    print()
    print("Hackerraank : Power-Mod Power")
    print()

    compute()

main()